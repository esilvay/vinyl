						<p>Wedding dresses come in a dizzying amount of options.  Here is a glossary of wedding dress terms to help you keep it all straight.</p><br />
                        <br />
                        <p><span style="color: #5FC9EC; font-family:Georgia; font-size:18px; font-weight:bold;">Silhouettes</span></p><br />
                        
                        <p><strong>A-line:</strong> fitted at the bodice and flows out to the ground, with an unbroken line (resembling the outline of an uppercase A).</p><br />

						<p><strong>Ballgown:</strong> fitted at the bodice and has a waistline that leads to a full skirt</p><br />

						<p><strong>Mermaid:</strong> fits closely to the body from the chest to the knee, then flares out to the hem</p><br />

						<p><strong>Sheath:</strong> fits very closely to the contours of the body. Has a straight skirt with no waist</p><br />

						<p><strong>Trumpet:</strong> fits closely to the body until the midhip and then widens gradually to the hem. (resembling the mouth of a trumpet).  Often confused with mermaid, but trumpet flairs from mid-hip, whereas mermaid flairs below the knee</p><br />
                        
                        
                        <p><span style="color: #5FC9EC; font-family:Georgia; font-size:18px; font-weight:bold;">Necklines</span></p><br />
                        
                        <p><strong>Portrait:</strong> (Off-the-Shoulder): sits below the shoulders and highlights the collar bone and shoulders</p><br />

                        <p><strong>Bateau: (boat neck):</strong> follows the curve of the collarbone to the very tip of  shoulders</p><br />
                        
                        <p><strong>Halter/High Neck:</strong> has straps that wrap around the back of the neck, or a high neck with deep armholes</p><br />
                        
                        <p><strong>Scoop:</strong> U shaped neckline with varying depth</p><br />
                        
                        <p><strong>V-neck:</strong>  dips down in a V-shape</p><br />
                        
                        <p><strong>Strapless:</strong> neckline with no straps.  Comes in 3 variations:</p><br />
                        
                             <p><span style="margin-left: 10px;">slight curve:  neckline curves slightly up or down</span></p>
                             <p><span style="margin-left: 10px;">straight across</span></p>
                             <p><span style="margin-left: 10px;">sweetheart: neckline is shaped like the top half of a heart</span></p><br />
                        
                        <p><strong>Spaghetti straps:</strong> Decorative (not functional) thin straps added to a strapless neckline.  Comes in the same 3 variations as strapless</p><br />
                        
                        <p><strong>Square:</strong> has a straight horizontal bodice that meets with straps in a 90 degree angle</p><br />
                        
                        
                        <p><span style="color: #5FC9EC; font-family:Georgia; font-size:18px; font-weight:bold;">Waistlines</span></p><br />

                        <p><strong>Basque Waist:</strong> waistline features a low U or V shape</p><br />
                        
                        <p><strong>Dropped Waist:</strong> features a waist line below the natural waist at the mid hip</p><br />
                        
                        <p><strong>Empire:</strong> has a raised waistline that sits just below the bust, from which the rest of the dress flows down to the hem</p><br />
                        
                        <p><strong>Natural Waist:</strong> waist line that hits at the natural waist (the indentation between the hips and the rib-cage)</p><br />
                        
                        <p><strong>Princess:</strong> A-line silhouette with vertical seams down the front</p><br />
                        
                        
                        <p><span style="color: #5FC9EC; font-family:Georgia; font-size:18px; font-weight:bold;">Trains</span></p><br />

                        <p><strong>Sweep:</strong> (also called brush) is the shortest  trains -usually, the back hem is only a few inches lower than the front hem</p><br />
                        
                        <p><strong>Court:</strong> slightly longer than the brush, extending approximately 3 feet behind the waist</p><br />
                        
                        <p><strong>Chapel Length:</strong> generally 1 1/3 yards or 4 feet from the waistline</p><br />
                        
                        <p><strong>Cathedral Length Train:</strong> generally 2 1/2 yards or 7 1/2 feet from the waistline</p><br />
                        
                        
                        <p><span style="color: #5FC9EC; font-family:Georgia; font-size:18px; font-weight:bold;">Fabrics</span></p><br />
                        
                        <p>A wedding dress can be made from either silk or polyester.  How the fiber is woven determines if the dress is:</p><br />
                        
                        <p><strong>Charmeuse:</strong> lightweight, semi-lustrous soft,  is satin-like to the touch</p><br />
                        
                        <p><strong>Chiffon:</strong> Delicate, sheer, and transparent, with a soft finish. Often layered because of its transparency</p><br />
                        
                        <p><strong>Crepe:</strong> Light, soft, and thin, with a crinkled surface</p><br />
                        
                        <p><strong>Duchesse Satin:</strong> A lightweight hybrid of silk or polyester and rayon woven into a satin finish</p><br />
                        
                        <p><strong>Dupioni:</strong> A finish similar to shantung, but with thicker, coarser fibers, and a slight sheen</p><br />
                        
                        <p><strong>Georgette:</strong> Sheer and lightweight fabric with a crepe surface</p><br />
                        
                        <p><strong>Mikado:</strong> A brand of blended fibers, usually heavier than 100-percent silk</p><br />
                        
                        <p><strong>Organza:</strong> Crisp and sheer like chiffon, with a stiffer texture similar, but more flowing than tulle</p><br />
                        
                        <p><strong>Satin:</strong> heavy and smooth with a high sheen on one side</p><br />
                        
                        <p><strong>Shantung:</strong> Similar to a raw silk, characterized by its rough/nubby texture</p><br />
                        
                        <p><strong>Taffeta:</strong> Crisp and smooth, with a slight rib</p><br />
                        
                        <p><strong>Tulle:</strong> Netting (just like ballerina tutus)</p><br />
                       
